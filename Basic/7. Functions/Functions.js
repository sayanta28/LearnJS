//Function is like a machine which can do a specific task.
function greet(){
    console.log('Hello Bro');
}
//Declaration of function without parameter.
greet();

function show(name, age){ // name is the parameter of the function
    console.log('Hello ' + name +'. Age:'+age);
}
//Declaration of function with parameter.
show('Sayanta',22); //Sayanta is a argument of the function
// Argument is actuully the supply of the function parameter.
show('Anik',32);


